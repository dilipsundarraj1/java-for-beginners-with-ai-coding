package com.modernjava._13abstract.payment;

public class AbstractionDemo {
    public static void main(String[] args) {
        PaymentProcessor creditCard = new CreditCardPaymentProcessor();
        PaymentProcessor paypal = new PaypalPaymentProcessor();
        PaymentProcessor giftCard = new GiftCardPaymentProcessor();

        creditCard.processPayment(150.0, "1234-5678-9012-3456");
        paypal.processPayment(75.5, "user@example.com");
        giftCard.processPayment(50.0, "GIFT-9876-5432");
    }
}

